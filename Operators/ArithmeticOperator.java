// Arithmetic operator in Java
// These operators perform fundamental arithmetic operations.
// + : Addition
// - : Subtraction
// * : Multiplication
// / : Division
// % : Modulus(remainder)

class ArithmeticOperator
{
    public static void main(String args[])
    {
        int a = 10, b = 20;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("b / a = " + (b / a));
        System.out.println("b % a = " + (b % a));
    }
}