// To declare a constant in java 'fianl' keyword is used.
// It is a customery to declare a constant in CAPITAL LETTERS.

public class Const
{
    public static void main(String args[])
    {
        final double PI = 3.14159;
        System.out.println("PI :"+PI);

        final int MAX = 100;
        System.out.println("MAX :"+MAX);

        final int MIN = 0;
        System.out.println("MIN :"+MIN);
    }   
}
